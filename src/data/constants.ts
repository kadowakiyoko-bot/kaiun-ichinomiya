// 称号
export function getShogo(count: number): { title: string; next?: number } {
  if (count >= 102) return { title: "大願成就 🏆" };
  if (count >= 80) return { title: "達人", next: 102 };
  if (count >= 50) return { title: "半達成", next: 80 };
  if (count >= 30) return { title: "巡拝者", next: 50 };
  if (count >= 10) return { title: "旅人", next: 30 };
  if (count >= 3) return { title: "入門", next: 10 };
  return { title: "これから", next: 3 };
}
