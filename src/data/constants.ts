export function goriyakuColor(label: string): string {
  if (GORIYAKU_COLORS[label]) return GORIYAKU_COLORS[label];
  for (const key of Object.keys(GORIYAKU_COLORS)) {
    if (label.includes(key)) return GORIYAKU_COLORS[key];
  }
  return "#BB3D20";
}

